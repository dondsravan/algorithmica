package com.algorithmica.advanced.tree;

public class TreeNode {

	Integer data;
	TreeNode left;
	TreeNode right;

	public TreeNode(Integer data) {
		this.data = data;
	}

	public Integer getData() {
		return data;
	}

	public TreeNode getLeft() {
		return left;
	}

	public TreeNode getRight() {
		return right;
	}
}
